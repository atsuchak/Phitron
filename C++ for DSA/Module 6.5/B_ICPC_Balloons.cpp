void solve() {
    int n; cin >> n;

    string st; cin >> st;
    sort(all(st));

    int cnt = 0;
    if(n >= 1) cnt = 1;
    
    for(int i = 0; i < n-1; i++) {
        if(st[i] != st[i+1]) cnt += 2;
        else cnt++;
    }

    cout << cnt+1 << endl;
}

